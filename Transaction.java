    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int pila = (Integer)jSpinner2.getValue();
        int[] available = MainClass.frame3.getStock();
        int current = available[0];
        if (pila < current) {
        if (pila > 0) {
            String item = product[0];
            double produce = price[0];
            String A = unit[0];
            double Total = produce * pila;
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[] {item,A,pila,Total});
            jSpinner2.setValue(0);
            counter++;
        }} else if ( pila > current) {
        JOptionPane.showMessageDialog(this,"Insufficient Stock!\nAvailable Milk: " + current + " Gallon", 
            "Stock Error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);}
        table();
    }//GEN-LAST:event_jButton1ActionPerformed

