    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        javax.swing.table.DefaultTableModel model1 = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        javax.swing.table.DefaultTableModel model2 = (javax.swing.table.DefaultTableModel) jTable2.getModel();
        String[] products = new String[model1.getRowCount()];
        int[] Qtys = new int[model1.getRowCount()];
        double[] totals = new double[model1.getRowCount()];
        for (int i = 0; i < model1.getRowCount(); i++) {
            products[i] = model1.getValueAt(i, 0).toString();
            Qtys[i] = Integer.parseInt(model1.getValueAt(i, 2).toString());
            totals[i] = Double.parseDouble(model1.getValueAt(i, 3).toString());
        }
        MainClass.frame2.setproduct(products);
        MainClass.frame2.setQty(Qtys);
        MainClass.frame2.settotal(totals);
        MainClass.frame2.input();
        if (MainClass.frame3 != null) {
        MainClass.frame3.updateStock(products, Qtys);
    }
        model1.setRowCount(0);
        model2.setRowCount(0);
        counter = 0;

        javax.swing.JOptionPane.showMessageDialog(this, "Transaction Saved to Arrays!");
    }//GEN-LAST:event_jButton5ActionPerformed

